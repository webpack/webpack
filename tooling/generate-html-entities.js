"use strict";

// cspell:disable

// Generates the `// #region html entities` block inside
// `lib/html/walkHtmlTokens.js` from the vendored WHATWG named character
// references table at `tooling/html-entities.json`.
//
// Run as part of `yarn lint:special` to verify the inlined block is in
// sync, or with `--write` (via `yarn fix:special`) to update it in place.
//
// To refresh `tooling/html-entities.json` against the current spec, run
// with `--fetch` (one-off, requires network access). Source URL:
// https://html.spec.whatwg.org/entities.json (WHATWG HTML Standard).

const fs = require("fs");
const https = require("https");
const path = require("path");

const SPEC_URL = "https://html.spec.whatwg.org/entities.json";
const FALLBACK_URL =
	"https://raw.githubusercontent.com/w3c/html/master/entities.json";
const DATA_PATH = path.resolve(__dirname, "html-entities.json");
const TARGET_PATH = path.resolve(
	__dirname,
	"..",
	"lib",
	"html",
	"walkHtmlTokens.js"
);

const REGION_REGEXP = /\/\/ #region html entities\n[\s\S]+?\/\/ #endregion\n/;

const doWrite = process.argv.includes("--write");
const doFetch = process.argv.includes("--fetch");

/**
 * @param {string} url URL to fetch
 * @returns {Promise<string>} fetched body
 */
const fetchUrl = (url) =>
	new Promise((resolve, reject) => {
		https
			.get(
				url,
				{ headers: { "user-agent": "webpack/generate-html-entities" } },
				(res) => {
					if (
						res.statusCode === 301 ||
						res.statusCode === 302 ||
						res.statusCode === 307 ||
						res.statusCode === 308
					) {
						return fetchUrl(/** @type {string} */ (res.headers.location)).then(
							resolve,
							reject
						);
					}
					if (res.statusCode !== 200) {
						return reject(
							new Error(`Failed to fetch ${url}: HTTP ${res.statusCode}`)
						);
					}
					/** @type {Buffer[]} */
					const chunks = [];
					res.on("data", (chunk) => chunks.push(chunk));
					res.on("end", () => resolve(Buffer.concat(chunks).toString("utf8")));
					res.on("error", reject);
				}
			)
			.on("error", reject);
	});

/**
 * @param {Record<string, { characters: string }>} entities raw WHATWG table
 * @returns {string} delta-encoded source string
 */
const encode = (entities) => {
	// Strip the leading `&` from each key; sort alphabetically so consecutive
	// entries share long prefixes (e.g. `Aacute;` / `Aacute` / `aacute;`).
	const pairs = Object.keys(entities)
		.map((k) => [k.slice(1), entities[k].characters])
		.sort();

	let source = "";
	let prev = "";
	for (const [name, chars] of pairs) {
		let shared = 0;
		const max = Math.min(name.length, prev.length);
		while (shared < max && name[shared] === prev[shared]) shared++;
		source += `${
			String.fromCharCode(0x20 + shared) + name.slice(shared)
		}\t${String.fromCharCode(0x30 + chars.length)}${chars}`;
		prev = name;
	}
	return source;
};

/**
 * Render the `// #region html entities` … `// #endregion` block.
 * @param {string} source delta-encoded entity table
 * @returns {string} replacement block (newline-terminated)
 */
const renderRegion = (source) =>
	`// #region html entities
// The contents of this region are auto-generated by
// \`tooling/generate-html-entities.js\` from \`tooling/html-entities.json\`.
// Do not edit by hand — re-run the generator (via \`yarn fix:special\`) to refresh.
//
// Delta-encoded WHATWG named character references. Each record in
// HTML_ENTITY_SOURCE is \`<sharedByte><suffix>\\t<valueLenByte><value>\`:
//   - sharedByte    = String.fromCharCode(0x20 + n) where n is the count
//                     of leading chars shared with the previous record's
//                     name (range 0x20..0x40).
//   - suffix        = the rest of this record's name.
//   - valueLenByte  = String.fromCharCode(0x30 + len) where len is the
//                     UTF-16 code-unit length of the value (1 or 2).
//   - value         = \`len\` UTF-16 code units of the decoded characters.
// Names are sorted alphabetically so adjacent entries share long prefixes.
// prettier-ignore
// cspell:disable-next-line
const HTML_ENTITY_SOURCE = ${JSON.stringify(source)};

/** @type {Readonly<Record<string, string>> | undefined} */
let htmlEntityCache;

/**
 * Named character references defined by the WHATWG HTML Standard. Decoded
 * lazily from \`HTML_ENTITY_SOURCE\` on first call and cached.
 * @returns {Readonly<Record<string, string>>} entity name to characters
 */
const getHtmlEntities = () => {
	if (htmlEntityCache !== undefined) return htmlEntityCache;
	/** @type {Record<string, string>} */
	const map = Object.create(null);
	let prev = "";
	let i = 0;
	while (i < HTML_ENTITY_SOURCE.length) {
		const shared = HTML_ENTITY_SOURCE.charCodeAt(i++) - 0x20;
		const t = HTML_ENTITY_SOURCE.indexOf("\t", i);
		const name = prev.slice(0, shared) + HTML_ENTITY_SOURCE.slice(i, t);
		const valueLen = HTML_ENTITY_SOURCE.charCodeAt(t + 1) - 0x30;
		map[name] = HTML_ENTITY_SOURCE.slice(t + 2, t + 2 + valueLen);
		prev = name;
		i = t + 2 + valueLen;
	}
	htmlEntityCache = Object.freeze(map);
	return htmlEntityCache;
};
// #endregion
`;

(async () => {
	if (doFetch) {
		let body;
		try {
			body = await fetchUrl(SPEC_URL);
		} catch (_err) {
			body = await fetchUrl(FALLBACK_URL);
		}
		const parsed = JSON.parse(body);
		fs.writeFileSync(DATA_PATH, `${JSON.stringify(parsed, null, 2)}\n`);
		console.error(`${path.relative(process.cwd(), DATA_PATH)} updated`);
	}

	const entities = JSON.parse(fs.readFileSync(DATA_PATH, "utf8"));
	const source = encode(entities);
	const region = renderRegion(source);

	const currentContent = fs.readFileSync(TARGET_PATH, "utf8");
	if (!REGION_REGEXP.test(currentContent)) {
		throw new Error(
			`Could not find the \`// #region html entities\` block in ${TARGET_PATH}`
		);
	}
	const newContent = currentContent.replace(REGION_REGEXP, region);

	if (newContent !== currentContent) {
		if (doWrite) {
			fs.writeFileSync(TARGET_PATH, newContent);
			console.error(
				`${path.relative(process.cwd(), TARGET_PATH)} updated (${Object.keys(entities).length} entities)`
			);
		} else {
			console.error(
				`${path.relative(process.cwd(), TARGET_PATH)} need to be updated`
			);
			process.exitCode = 1;
		}
	}
})().catch((err) => {
	throw err;
});
